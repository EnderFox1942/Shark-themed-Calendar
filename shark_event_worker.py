import random
import asyncio
import logging
from datetime import datetime, date
from typing import Optional

# ── Try python-dotenv, fall back to manual loader ──────────────────────────────
try:
    from dotenv import load_dotenv
    load_dotenv()
except ImportError:
    def load_dotenv():  # noqa
        if not os.path.exists(".env"):
            return
        with open(".env") as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    k, v = line.split("=", 1)
                    os.environ.setdefault(k.strip(), v.strip().strip('"').strip("'"))
    load_dotenv()

from supabase import create_client, Client

# ── Logging ────────────────────────────────────────────────────────────────────
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s  %(levelname)-8s  %(message)s",
    datefmt="%Y-%m-%d %H:%M:%S",
)
log = logging.getLogger("SharkWorker")

# ── Config ─────────────────────────────────────────────────────────────────────
EVENTS_TABLE   = "shark_events"
WORKER_TAG     = "[WORKER]"          # Prefix added to every worker-created event title
                                     # — used to identify which events to clean up.

CREATE_MIN_SEC = 10 * 60             # 10 minutes  (minimum gap between creations)
CREATE_MAX_SEC = 20 * 60             # 20 minutes  (maximum gap between creations)
DELETE_SEC     = 5  * 60             # 5  minutes  (how often the cleanup runs)

# PostgreSQL / Python max date is 9999-12-31
FAR_DATE       = date(9999, 12, 31)

# ── Flavour pools ──────────────────────────────────────────────────────────────
