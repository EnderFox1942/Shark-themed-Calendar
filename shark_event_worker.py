"""
🦈 Shark Calendar - Auto Event Worker
Creates a random event on a far-off date every 10-20 minutes.
Deletes worker-created events every 5 minutes.

Requires: supabase
Install:  pip install supabase python-dotenv
Usage:    python shark_event_worker.py
"""

import os
