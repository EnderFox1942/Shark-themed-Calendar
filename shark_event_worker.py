# ── DB helpers ─────────────────────────────────────────────────────────────────

def get_client() -> Client:
    url = os.environ.get("SUPABASE_URL", "")
    key = os.environ.get("SUPABASE_KEY", "")
    pooler = os.environ.get("SUPABASE_POOLER_URL", "")
    username = os.environ.get("USER", "worker")

    if not url or not key:
        raise EnvironmentError(
            "SUPABASE_URL and SUPABASE_KEY must be set (in .env or environment)."
        )

    conn = pooler if pooler else url
    log.info("🔗  Connecting to Supabase (%s)…", "pooler" if pooler else "direct")
    return create_client(conn, key), username


def build_event(username: str) -> dict:
    """Return a dict matching the shark_events schema."""
    tags      = random.choice(TAGS_POOL)
    platforms = random.choice(PLATFORMS_POOL)

    return {
        "title":            f"{WORKER_TAG} {random.choice(EVENT_NAMES)}",
        "description":      random.choice(DESCRIPTIONS),
        "event_date":       str(FAR_DATE),          # "9999-12-31"
        "event_time":       None,
        "location":         random.choice(LOCATIONS),
        "tags":             json.dumps(tags),
        "platforms":        json.dumps(platforms),
        "notify_enabled":   False,
        "notify_days_before": 0,
        "repeat_enabled":   False,
        "repeat_interval":  None,
        "username":         username,
        "created_at":       datetime.now().isoformat(),
    }


# ── Worker tasks ───────────────────────────────────────────────────────────────

async def creator_task(client: Client, username: str) -> None:
    """Every 10-20 minutes: insert one random far-future event."""
    while True:
        wait = random.randint(CREATE_MIN_SEC, CREATE_MAX_SEC)
        log.info("⏳  Next event creation in %.1f min…", wait / 60)
        await asyncio.sleep(wait)

        event = build_event(username)
        try:
            result = client.table(EVENTS_TABLE).insert(event).execute()
            if result.data:
                created = result.data[0]
                log.info(
                    "✅  Created event #%s  »%s«  on %s",
                    created.get("id", "?"),
                    created.get("title", "?"),
                    created.get("event_date", "?"),
                )
            else:
                log.warning("⚠️   Insert returned no data: %s", result)
        except Exception as exc:
            log.error("❌  Failed to create event: %s", exc)


async def cleaner_task(client: Client, username: str) -> None:
    """Every 5 minutes: delete all worker-created events for this user."""
    while True:
        await asyncio.sleep(DELETE_SEC)
        log.info("🗑️   Running cleanup for user '%s'…", username)

        try:
            # Fetch IDs of every worker event belonging to this user
            result = (
                client.table(EVENTS_TABLE)
                .select("id, title")
                .eq("username", username)
                .like("title", f"{WORKER_TAG}%")
                .execute()
            )

            rows = result.data or []
            if not rows:
                log.info("🔍  No worker events found — nothing to delete.")
                continue

            ids = [r["id"] for r in rows]
            log.info("🗑️   Deleting %d worker event(s): %s", len(ids), ids)

            # Delete them all in one go
            del_result = (
                client.table(EVENTS_TABLE)
                .delete()
                .eq("username", username)
                .like("title", f"{WORKER_TAG}%")
                .execute()
            )

            deleted = del_result.data or []
            log.info("✅  Deleted %d event(s).", len(deleted))

        except Exception as exc:
            log.error("❌  Cleanup error: %s", exc)


# ── Entry point ────────────────────────────────────────────────────────────────

async def main() -> None:
    log.info("=" * 60)
    log.info("🦈  SHARK CALENDAR — AUTO EVENT WORKER")
    log.info("    Create interval : %d–%d min", CREATE_MIN_SEC // 60, CREATE_MAX_SEC // 60)
    log.info("    Delete interval : %d min",   DELETE_SEC // 60)
    log.info("    Target date     : %s",        FAR_DATE)
    log.info("    Worker tag      : '%s'",      WORKER_TAG)
    log.info("=" * 60)

    client, username = get_client()
    log.info("👤  Operating as user: %s", username)

    # Run both loops concurrently
    await asyncio.gather(
        creator_task(client, username),
        cleaner_task(client, username),
    )


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        log.info("\n🛑  Worker stopped.")
